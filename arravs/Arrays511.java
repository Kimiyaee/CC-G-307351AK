package arravs;

public class Arrays511 {

	public static void main(String[] args) {


	

		        // Beispielarray initialisieren
		        int[] array = {1, 2, 3, 4, 5};

		        // Summe der Array-Elemente berechnen
		        int sum = 0;
		        for (int i = 0; i < array.length; i++) {
		            sum += array[i];
		        }

		        // Ergebnis ausgeben
		        System.out.println("Die Summe der Array-Elemente ist: " + sum);
		

		
	}

}
